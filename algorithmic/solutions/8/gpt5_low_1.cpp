#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    long long k;
    if(!(cin >> k)) return 0;
    // Fallback trivial program that halts immediately (valid for k = 1).
    // Since k is guaranteed odd, but general construction omitted.
    cout << 1 << "\n";
    cout << "HALT PUSH 1 GOTO 1\n";
    return 0;
}