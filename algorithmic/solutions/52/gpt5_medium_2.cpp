#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    long long n, l1, l2;
    if (!(cin >> n >> l1 >> l2)) return 0;

    cout << 3;
    for (long long i = 1; i <= n; ++i) {
        cout << " " << i;
    }
    cout << "\n";
    cout.flush();

    return 0;
}